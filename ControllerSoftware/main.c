void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim)
{
	uint8_t axis;
	if(htim->Instance == TIM1) axis = 0;
	else if(htim->Instance==TIM4) axis = 1;
	else if(htim->Instance==TIM3) axis = 2;
	else if(htim->Instance==TIM8) axis = 3;
	else if(htim->Instance==TIM9) axis = 4;
	else if(htim->Instance==TIM10) axis = 5;
	else return;

	if(step_goal[axis]>=step_pos[axis]) dir_goal[axis] = 0;
	else dir_goal[axis] = 1;

	step_nr[axis]++;
	if(dir[axis]) step_pos[axis]--;
	else step_pos[axis]++;

	if(acc_steps[axis] == 0)
	{
		delay[axis] = maxDelay[axis];
		dir[axis] = dir_goal[axis];
	}

	if(step_goal[axis] == step_pos[axis] && acc_steps[axis] <= 1)
	{
		htim->Instance->CR1 &= ~(TIM_CR1_CEN);
		delay[axis] = maxDelay[axis];
	}
	//else if(step_nr[axis]>steps_to_do[axis]-acc_steps[axis] && step_nr[axis] < steps_to_do[axis])
	else if(dir[axis] != dir_goal[axis] || abs(step_goal[axis]-step_pos[axis]) <= acc_steps[axis])
	{
		tempDelay[axis] = delay[axis]+((2*delay[axis])/(4*(acc_steps[axis]) + 1));
		acc_steps[axis]--;

		delay[axis] = tempDelay[axis];
		arrtim2[axis] = delay[axis];

		htim->Instance->ARR = arrtim2[axis];
		htim->Instance->CCR1 = arrtim2[axis] >> 1;
	}
	//if(steps_to_do[axis]-step_nr[axis] > acc_steps[axis] && delay[axis]>maxSpeed[axis])
	else if((abs(step_goal[axis]-step_pos[axis])) > acc_steps[axis] && delay[axis]>maxSpeed[axis])
	{
		acc_steps[axis]++;
		tempDelay[axis] = delay[axis]-((2*delay[axis])/(4*acc_steps[axis] + 1));

		delay[axis] = tempDelay[axis];
		arrtim2[axis] = delay[axis];

		htim->Instance->ARR = arrtim2[axis];
		htim->Instance->CCR1 = arrtim2[axis] >> 1;
	}


	//if(step_nr[axis]>=steps_to_do[axis])
	/*if(step_goal[axis] == step_pos[axis])
	{
		htim->Instance->CR1 &= ~(TIM_CR1_CEN);
		delay[axis] = 200000;
		step_nr[axis] = 0;
		step_to_do[axis] = 0;
	}*/
	//if(step_nr[1]>=steps_to_do[1]) TIM2->CR1 &= ~(TIM_CR1_CEN);

}
